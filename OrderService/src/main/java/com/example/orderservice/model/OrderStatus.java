package com.example.orderservice.model;


/**
 * Статусы заказа отражают полный жизненный цикл от оформления до завершения/отмены.
 */
public enum OrderStatus {

    /**
     * Заказ только создан (черновик).
     * Пользователь добавил товары, но ещё не подтвердил оплату.
     */
    NEW,

    /**
     * Заказ подтверждён пользователем и отправлен на оплату.
     * Ожидается успешное завершение платежа или подтверждение от платёжного сервиса.
     */
    PENDING_PAYMENT,

    /**
     * Платёж успешно прошёл.
     * Можно приступать к комплектации/передаче заказа на склад.
     */
    PAID,

    /**
     * Заказ принят в обработку (комплектация, проверка склада, подготовка к отгрузке).
     * Используется после успешной оплаты до момента фактической отправки.
     */
    PROCESSING,

    /**
     * Заказ отправлен клиенту (есть трек-номер / передан в службу доставки).
     * Ожидается доставка.
     */
    SHIPPED,

    /**
     * Заказ доставлен клиенту и получен им.
     * Финальный положительный статус.
     */
    DELIVERED,

    /**
     * Заказ отменён до оплаты или до отправки.
     * Применяется по инициативе клиента или системы.
     */
    CANCELLED,

    /**
     * По заказу оформлен возврат средств или возврат товара.
     * Применяется после PAID/SHIPPED/DELIVERED, если была операция возврата.
     */
    REFUNDED
}
